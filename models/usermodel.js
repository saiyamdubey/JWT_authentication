const mongoose = require("mongoose");

const uri = process.env.MONGOOSE_KEY;

const Userschema = mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const User = mongoose.model("UserList", Userschema);

module.exports = User;
